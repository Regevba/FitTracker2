#!/usr/bin/env node
// scripts/check-tokens-android.js — CI gate: verify the generated Android token
// artifacts are in sync with design-tokens/tokens.json. Mirrors check-tokens.js
// (the iOS gate): regenerate in place, compare (date lines stripped), restore.
//
// Uses execFileSync with a fixed argv (no shell) — there is no dynamic/user
// input in this script, so there is no command-injection surface.

const { execFileSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const TARGETS = [
  "android/FitMeDesignTokens.kt",
  "android/res/values/colors.xml",
  "android/res/values/dimens.xml",
].map((p) => path.join(ROOT, p));

// Save committed versions
const committed = TARGETS.map((t) => fs.readFileSync(t, "utf8"));

// Regenerate (fixed argv, no shell)
try {
  execFileSync("npm", ["run", "tokens:android"], { stdio: "inherit", cwd: ROOT });
} catch (e) {
  console.error("❌  tokens:android build failed:", e.message);
  process.exit(1);
}

// Read freshly generated, then restore committed immediately
const generated = TARGETS.map((t) => fs.readFileSync(t, "utf8"));
TARGETS.forEach((t, i) => fs.writeFileSync(t, committed[i], "utf8"));

// Strip the AUTO-GENERATED date line (Kotlin `//` + XML `<!-- -->`) before diffing
function strip(s) {
  return s.replace(/AUTO-GENERATED by `make tokens-android` on \d{4}-\d{2}-\d{2}/g, "DATE");
}

let drift = false;
TARGETS.forEach((t, i) => {
  if (strip(committed[i]) !== strip(generated[i])) {
    drift = true;
    console.error(`❌  ${path.relative(ROOT, t)} is OUT OF SYNC with tokens.json.`);
  }
});

if (drift) {
  console.error("    Run `make tokens-android` (or `npm run tokens:android`) and commit the result.");
  process.exit(1);
}
console.log("✅  Android token artifacts are in sync with tokens.json");
process.exit(0);
